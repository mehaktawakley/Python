"""
Given a number, write a program to find a maximum number that can be formed using all of the digits of this number.

Input:
The first line of input contains an integer T denoting the number of test cases. Then T test cases follow. The first line of each test case contains the number N.

Output:
Print the largest number that can be formed using the digits of the given number. Print the answer for each test case in a new line.

Constraints:
1<= T <=100
1<= N <=101000

Example:
Input:
2
38293367
1203465

Output:
98763332
6543210
"""

count = int(input())

while count>0:
    n = list(input())
    n.sort(reverse=True)
    print ("".join(n))
    count-=1
